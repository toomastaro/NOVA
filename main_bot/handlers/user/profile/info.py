async def show_info_menu(call: types.CallbackQuery):
    """–ü–æ–∫–∞–∑–∞—Ç—å –º–µ–Ω—é –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏"""
    await call.message.delete()
    await call.message.answer(
        text("info:menu"),
        reply_markup=keyboards.info_menu(),
        parse_mode="HTML"
    )


